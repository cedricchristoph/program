    private void nextBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextBtn1ActionPerformed
        fileChooser.setCurrentDirectory(new File("."));
        fileChooser.showSaveDialog(this);
        logger = new Logger(fileChooser.getSelectedFile());
        if (logger.save(partida)) 
            JOptionPane.showMessageDialog(this, "Game has been saved succesfully", "Game saved", JOptionPane.INFORMATION_MESSAGE);
        else
            JOptionPane.showMessageDialog(this, "Error while trying to save the game. Game hasn't been saved.", "Error", JOptionPane.ERROR_MESSAGE);
        tabs.setSelectedIndex(0);
        reset();
    }//GEN-LAST:event_nextBtn1ActionPerformed

