    /**
     * Method used for switching player turn
     * @since 1.0.0
     */
    public void switchTurn() {
        player.disable();
        partida.switchTurn();
        showTurn();
        cargarHistorial();
        player.enable();
    }
    
    /**
     * Method used for showing which player has the turn
     * @since 1.0.0
     */
    public void showTurn() {
        if (partida.getTurn() == partida.TURN_PLAYER_ONE())
            turnLabel.setText("Jugador 1");
        else
            turnLabel.setText("Jugador 2");
    }
    
    /**
     * Method used to properly load each players previous moves
     * @since 1.0.0
     */
    public void cargarHistorial() {
        try {
            iLoader = new IconLoader();
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(Display.class.getName()).log(Level.SEVERE, null, ex);
        }
        Field[][] map = partida.getActivePlayground().getMap();
        JButton[][] botones = getButtonList();
        for (int i=0; i<8; i++) {
            for (int j=0; j<8; j++) {
                String valor = map[i][j].toString();
                switch(valor){
                    case "X":
                        botones[i][j].setIcon(iLoader.getExplosion(botones[i][j].getWidth(), botones[i][j].getHeight()));
                        break;
                    case "0":
                        botones[i][j].setIcon(iLoader.getWater(botones[i][j].getWidth(), botones[i][j].getHeight()));
                        break;
                    case " ":
                        botones[i][j].setIcon(iLoader.getGreyWater(botones[i][j].getWidth(), botones[i][j].getHeight()));
                        break;
                }
                //botones[i][j].setText(valor);
            }
        }
        if (lastHitted) {
            partida.setFinished(false);
            player.explosion();
        } else {
            player.water();
        }
        
        if (partida.getFinished())
            nextBtn.setEnabled(true);
        else
            nextBtn.setEnabled(false);   
        
        if (partida.allShipsDestroyed()) {
            winnerLabel.setText("Los barcos de " + partida.getActivePlayground().getPlayer() + " han sido destruidos !!!");
            tabs.setSelectedIndex(4);
            player.victory();
        }
    }

